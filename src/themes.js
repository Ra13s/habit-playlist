export const applyTheme = (themeName) => {
  const theme = themes[themeName]
  if (!theme) return

  const root = document.documentElement

  // Apply colors
  Object.entries(theme.colors).forEach(([key, value]) => {
    const cssVar = key.replace(/([A-Z])/g, '-$1').toLowerCase()
    root.style.setProperty(`--${cssVar}`, value)
  })

  // Apply fonts
  root.style.setProperty('--font-primary', theme.fonts.primary)
  root.style.setProperty('--font-secondary', theme.fonts.secondary)

  // Apply other styles
  root.style.setProperty('--border-radius', theme.borderRadius)
  root.style.setProperty('--shadow', theme.shadow)
  root.style.setProperty('--shadow-lg', theme.shadowLg)

  // Apply button style class
  root.setAttribute('data-button-style', theme.buttonStyle)

  // Apply noise effect
  root.setAttribute('data-noise', theme.noise ? 'true' : 'false')
}

export const getThemeName = (key) => {
  return themes[key]?.name || key
}
